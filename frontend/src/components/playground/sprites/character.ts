// ── Frame composition ──
const mk = (h: Frame, t: Frame, l: Frame): Frame => [...h, ...t, ...l];
const mirror = (f: Frame): Frame => f.map(r => [...r].reverse());

/** 에이전트 코드 → 헤어스타일 인덱스 (결정적 해시) */
function hairIndex(agentCode: string): number {
  if (agentCode === 'JINXUS_CORE') return 6; // CEO 전용
  let h = 0;
  for (let i = 0; i < agentCode.length; i++) h = ((h << 5) - h + agentCode.charCodeAt(i)) | 0;
  return Math.abs(h) % 6; // 0-5 중 선택
}

/** 에이전트별 헤드 프레임 반환 */
function headDown(agent: string): Frame { return HAIR_STYLES[hairIndex(agent)].d; }
function headUp(agent: string): Frame { return HAIR_STYLES[hairIndex(agent)].u; }
function headLeft(agent: string): Frame { return HAIR_STYLES[hairIndex(agent)].l; }

/** CEO 전용 torso 선택 */
function torsoFront(agent: string): Frame {
  return agent === 'JINXUS_CORE' ? TF_CEO : TF;
}

export function walkFrame(dir: number, step: number, agent: string = ''): Frame {
  const i = WALK_SEQ[step % 4];
  if (dir === 0) return mk(headDown(agent), torsoFront(agent), [L0, LA, LB][i]);
  if (dir === 3) return mk(headUp(agent), torsoFront(agent), [L0, LA, LB][i]);
  const f = mk(headLeft(agent), TS, [SL0, SLA, SLB][i]);
  return dir === 2 ? mirror(f) : f;
}

export function typeFrame(i: number, agent: string = ''): Frame {
  return mk(headDown(agent), i === 0 ? TY0 : TY1, L0);
}

export function readFrame(i: number, agent: string = ''): Frame {
  return mk(headDown(agent), i === 0 ? TR0 : TR1, L0);
}

export function thinkFrame(i: number, agent: string = ''): Frame {
  return mk(headDown(agent), i === 0 ? TK0 : TK1, L0);
}

