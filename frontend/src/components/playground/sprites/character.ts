// ── Frame composition ──
const mk = (h: Frame, t: Frame, l: Frame): Frame => [...h, ...t, ...l];
const mirror = (f: Frame): Frame => f.map(r => [...r].reverse());

export function walkFrame(dir: number, step: number): Frame {
  const i = WALK_SEQ[step % 4];
  if (dir === 0) return mk(HD, TF, [L0, LA, LB][i]);
  if (dir === 3) return mk(HU, TF, [L0, LA, LB][i]);
  const f = mk(HL, TS, [SL0, SLA, SLB][i]);
  return dir === 2 ? mirror(f) : f;
}

export function typeFrame(i: number): Frame {
  return mk(HD, i === 0 ? TY0 : TY1, L0);
}

export function readFrame(i: number): Frame {
  return mk(HD, i === 0 ? TR0 : TR1, L0);
}

export function thinkFrame(i: number): Frame {
  return mk(HD, i === 0 ? TK0 : TK1, L0);
}
